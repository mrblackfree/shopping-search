import { 
  createStealthBrowser, 
  createStealthPage, 
  randomDelay, 
  waitForAnySelector, 
  scrollToLoadContent,
  retryWithBackoff,
  getRandomUserAgent
} from '../playwright-utils';

export async function searchDHgate(keyword: string): Promise<SearchResult> {
  const startTime = Date.now();
  
  return await retryWithBackoff(async () => {
    const browser = await createStealthBrowser();
    let page;
    
    try {
      console.log('üîç DHgate Í≤ÄÏÉâ ÏãúÏûë:', keyword);
      
      page = await createStealthPage(browser);
      
      // DHgate Ï†ÑÏö© ÏÑ§Ï†ï
      await page.addInitScript(() => {
        Object.defineProperty(navigator, 'userAgent', {
          get: () => 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36'
        });
      });

      const searchUrl = `https://www.dhgate.com/wholesale/search.do?searchkey=${encodeURIComponent(keyword)}&catalog=`;
      
      // ÌéòÏù¥ÏßÄ Î°úÎìú
      await page.goto(searchUrl, { 
        waitUntil: 'networkidle', 
        timeout: 30000 
      });

      // ÏûêÏó∞Ïä§Îü¨Ïö¥ ÎîúÎ†àÏù¥
      await randomDelay(3000, 5000);

      // ÏÉÅÌíà Î¶¨Ïä§Ìä∏Í∞Ä Î°úÎìúÎê† ÎïåÍπåÏßÄ ÎåÄÍ∏∞
      const productSelectors = [
        '.stpro',
        '.search-prd',
        '.goods-item',
        '.product-item',
        '.item-info'
      ];

      const foundSelector = await waitForAnySelector(page, productSelectors, 15000);
      
      if (!foundSelector) {
        // Ïä§ÌÅ¨Î°§ÌïòÏó¨ ÎèôÏ†Å ÏΩòÌÖêÏ∏† Î°úÎìú ÏãúÎèÑ
        console.log('ÏÉÅÌíà Î¶¨Ïä§Ìä∏ Î°úÎî© Ï§ë... Ïä§ÌÅ¨Î°§ ÏãúÎèÑ');
        await scrollToLoadContent(page);
        await randomDelay(3000, 5000);
      }

      // ÏÉÅÌíà Ï†ïÎ≥¥ Ï∂îÏ∂ú
      const products = await page.evaluate(() => {
        const productElements = document.querySelectorAll('.stpro, .search-prd, .goods-item, .product-item, .item-info');
        const results: any[] = [];

        console.log(`Î∞úÍ≤¨Îêú ÏÉÅÌíà ÏöîÏÜå Ïàò: ${productElements.length}`);

        productElements.forEach((element, index) => {
          if (index >= 5) return; // ÏÉÅÏúÑ 5Í∞úÎßå

          try {
            // Ï†úÎ™© Ï∂îÏ∂ú (Ïó¨Îü¨ ÏÖÄÎ†âÌÑ∞ ÏãúÎèÑ)
            const titleSelectors = [
              '.item-title a',
              '.product-title a',
              'h2 a',
              'h3 a',
              'a[title]',
              '.goods-title a'
            ];
            
            let title = '';
            let productUrl = '';
            for (const selector of titleSelectors) {
              const titleEl = element.querySelector(selector) as HTMLAnchorElement;
              if (titleEl) {
                title = titleEl.textContent?.trim() || titleEl.getAttribute('title')?.trim() || '';
                productUrl = titleEl.href || '';
                if (title) break;
              }
            }

            // Í∞ÄÍ≤© Ï∂îÏ∂ú
            const priceSelectors = [
              '.item-price',
              '.price-current',
              '.price-now',
              '.cost',
              '.price'
            ];
            
            let priceText = '';
            for (const selector of priceSelectors) {
              const priceEl = element.querySelector(selector);
              if (priceEl) {
                priceText = priceEl.textContent?.trim() || '';
                if (priceText) break;
              }
            }

            // Ïù¥ÎØ∏ÏßÄ Ï∂îÏ∂ú
            const imgSelectors = [
              '.item-pic img',
              '.product-image img',
              '.goods-image img',
              'img'
            ];
            
            let imageUrl = '';
            for (const selector of imgSelectors) {
              const imgEl = element.querySelector(selector) as HTMLImageElement;
              if (imgEl) {
                imageUrl = imgEl.src || imgEl.getAttribute('data-src') || '';
                if (imageUrl && !imageUrl.includes('placeholder')) break;
              }
            }

            // ÌåêÎß§Ïûê Ï†ïÎ≥¥
            const sellerSelectors = ['.seller-name', '.store-name', '.shop-name'];
            let sellerName = '';
            for (const selector of sellerSelectors) {
              const sellerEl = element.querySelector(selector);
              if (sellerEl) {
                sellerName = sellerEl.textContent?.trim() || '';
                if (sellerName) break;
              }
            }

            // ÌèâÏ†ê Ï†ïÎ≥¥
            const ratingSelectors = ['.seller-rating', '.store-rating', '.rating'];
            let ratingText = '';
            for (const selector of ratingSelectors) {
              const ratingEl = element.querySelector(selector);
              if (ratingEl) {
                ratingText = ratingEl.textContent?.trim() || '';
                if (ratingText) break;
              }
            }

            if (title && priceText) {
              // Í∞ÄÍ≤© ÌååÏã±
              const priceMatch = priceText.match(/\$?([\d.,]+)/);
              const price = priceMatch ? parseFloat(priceMatch[1].replace(',', '')) : 0;

              // ÌèâÏ†ê ÌååÏã±
              const ratingMatch = ratingText.match(/([\d.]+)/);
              const rating = ratingMatch ? parseFloat(ratingMatch[1]) : undefined;

              if (price > 0) {
                results.push({
                  id: `dhgate_${index}`,
                  title,
                  price,
                  currency: 'USD',
                  imageUrl: imageUrl ? (imageUrl.startsWith('http') ? imageUrl : `https:${imageUrl}`) : '',
                  productUrl: productUrl ? (productUrl.startsWith('http') ? productUrl : `https://www.dhgate.com${productUrl}`) : '',
                  seller: {
                    name: sellerName,
                    rating,
                    trustLevel: rating && rating > 4.5 ? 'High' : rating && rating > 3.5 ? 'Medium' : 'Low'
                  },
                  site: 'dhgate',
                  minOrder: 1,
                  shipping: 'Free Shipping'
                });
              }
            }
          } catch (error) {
            console.error(`ÏÉÅÌíà ${index} ÌååÏã± Ïò§Î•ò:`, error);
          }
        });

        return results;
      });

      // KRW Î≥ÄÌôò
      const productsWithKRW = await Promise.all(
        products.map(async (product: any) => ({
          ...product,
          priceKRW: await convertToKRW(product.price, 'USD')
        }))
      );

      console.log(`‚úÖ DHgateÏóêÏÑú ${productsWithKRW.length}Í∞ú ÏÉÅÌíà Î∞úÍ≤¨`);
      
      return {
        query: keyword,
        totalResults: productsWithKRW.length,
        products: productsWithKRW,
        site: 'dhgate',
        searchTime: Date.now() - startTime
      };

    } catch (error) {
      console.error('‚ùå DHgate Í≤ÄÏÉâ Ïò§Î•ò:', error);
      return {
        query: keyword,
        totalResults: 0,
        products: [],
        site: 'dhgate',
        searchTime: Date.now() - startTime
      };
    } finally {
      if (page) await page.close();
      await browser.close();
    }
  });
}

export async function analyzeDHgateProduct(url: string): Promise<any> {
  return await retryWithBackoff(async () => {
    const browser = await createStealthBrowser();
    let page;
    
    try {
      console.log('üîç DHgate ÏÉÅÌíà Î∂ÑÏÑù:', url);
      
      page = await createStealthPage(browser);
      
      await page.goto(url, { 
        waitUntil: 'networkidle', 
        timeout: 30000 
      });

      await randomDelay(3000, 5000);

      const productData = await page.evaluate(() => {
        // Ï†úÎ™© Ï∂îÏ∂ú
        const titleSelectors = [
          '.product-name h1',
          '.item-title',
          'h1',
          '.goods-title'
        ];
        
        let title = '';
        for (const selector of titleSelectors) {
          const el = document.querySelector(selector);
          if (el) {
            title = el.textContent?.trim() || '';
            if (title) break;
          }
        }

        // Í∞ÄÍ≤© Ï∂îÏ∂ú
        const priceSelectors = [
          '.price-now',
          '.current-price',
          '.item-price',
          '.price'
        ];
        
        let priceText = '';
        for (const selector of priceSelectors) {
          const el = document.querySelector(selector);
          if (el) {
            priceText = el.textContent?.trim() || '';
            if (priceText) break;
          }
        }

        // ÏÑ§Î™Ö Ï∂îÏ∂ú
        const descSelectors = [
          '.product-description',
          '.item-description',
          '.goods-description'
        ];
        
        let description = '';
        for (const selector of descSelectors) {
          const el = document.querySelector(selector);
          if (el) {
            description = el.textContent?.trim() || '';
            if (description) break;
          }
        }

        // ÌåêÎß§Ïûê Ï†ïÎ≥¥
        const sellerSelectors = [
          '.seller-info .name',
          '.store-name',
          '.shop-name'
        ];
        
        let sellerName = '';
        for (const selector of sellerSelectors) {
          const el = document.querySelector(selector);
          if (el) {
            sellerName = el.textContent?.trim() || '';
            if (sellerName) break;
          }
        }

        // Ïù¥ÎØ∏ÏßÄ ÏàòÏßë
        const imageUrls: string[] = [];
        const imgElements = document.querySelectorAll('.product-gallery img, .item-images img, .goods-images img');
        imgElements.forEach(img => {
          const src = (img as HTMLImageElement).src || img.getAttribute('data-src');
          if (src && !src.includes('placeholder')) {
            imageUrls.push(src.startsWith('//') ? `https:${src}` : src);
          }
        });

        const priceMatch = priceText.match(/\$?([\d.,]+)/);
        const price = priceMatch ? parseFloat(priceMatch[1].replace(',', '')) : 0;

        return {
          title,
          description,
          price,
          currency: 'USD',
          seller: {
            name: sellerName
          },
          site: 'dhgate',
          imageUrls,
          originalUrl: window.location.href
        };
      });

             const finalProductData = {
         ...productData,
         priceKRW: productData.price > 0 ? await convertToKRW(productData.price, 'USD') : 0
       };

             console.log('‚úÖ DHgate ÏÉÅÌíà Î∂ÑÏÑù ÏôÑÎ£å');
       return finalProductData;

    } catch (error) {
      console.error('‚ùå DHgate ÏÉÅÌíà Î∂ÑÏÑù Ïò§Î•ò:', error);
      throw new Error('DHgate ÏÉÅÌíà Ï†ïÎ≥¥Î•º Í∞ÄÏ†∏Ïò¨ Ïàò ÏóÜÏäµÎãàÎã§.');
    } finally {
      if (page) await page.close();
      await browser.close();
    }
  });
} 